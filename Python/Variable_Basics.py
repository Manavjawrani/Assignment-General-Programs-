# Variables can hold integer numbers and decimal/floating point numbers
a = 5
b = 10.50

print("a =", a, "\nType: ", type(a), "\n")
print("b =", b, "\nType: ", type(b), "\n")

# Variables can also hold strings
name = "Tom"
print("name =", name, "\nType: ", type(name), "\n")

# Variables can also be assigned to other variables
x = 100
y = x
z = y
print("z =", z, "\nType: ", type(z), "\n")