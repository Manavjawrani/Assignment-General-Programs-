animal_list = ["cat","dog","elephant","cow","man"]

# for loop can be used to iterate over a list in python
print("Printing animals from animal list")
for animal in animal_list:
    print(animal)

# range function provides a list of integers from 0 to n-1, where n is the number provided by user.
print("\nPrinting numbers from 0-9")
for i in range(10):
    print(i)