.MODEL SMALL
.STACK 100H
.DATA
ARRAY DB 55, 32, 98, 21, 13, 16, 38, 25, 56, 12
SMALLEST DB ?
.CODE
MAIN PROC
    MOV AX, @DATA
    MOV DS, AX
    MOV BX, 0
    MOV AL, ARRAY[BX]
    MOV SMALLEST, AL
    COMPARE:
    INC BX
    CMP BX, 10
    JE EXIT
    MOV AL, ARRAY[BX]
    CMP AL, SMALLEST
    JL UPDATE_SMALLEST
    JMP COMPARE
    UPDATE_SMALLEST:
    MOV SMALLEST, AL
    JMP COMPARE
    EXIT:
    MAIN ENDP
END MAIN